#include <iostream.h>	//cout
#include <iomanip.h>		//setw
#include <conio.h>		//getch()
void main()
{ const int f=6; 			//]w块X逆e
  float pi=3.14159;
  cout << "p计旒2,逆e"<< f << " tp计I" << endl;
  cout.setf(ios::fixed,ios::floatfield);	//wIp计
  cout.precision(2);		//]wp计旒2
  cout << setw(f) << pi << endl;
  cout << setw(f) << pi/4 << endl;
  cout << setw(f) << pi*10*10 << endl;
  getch();	//既氨
}
